package com.problem;

import java.util.Arrays;

public class leetcode_414 {

/*
 * 414. 第三大的数难度简单160给定一个非空数组，返回此数组中第三大的数。如果不存在，则返回数组中最大的数。要求算法时间复杂度必须是O(n)。
	示例 1:
	输入: [3, 2, 1]
	输出: 1
	解释: 第三大的数是 1.*/
	public static void main(String[] args) {
		int[] nums = {4,3,3,2,2,1,0};
		int n = thirdMax(nums);
		System.out.println(n);
	}
    public static int thirdMax(int[] nums) {
    	Arrays.sort(nums);
    	int t=1,i;
    	for(i=nums.length-2;i>=0;i--) {
    		if(nums[i]<nums[i+1]) {
    			t++;
    		}
    		if(t==3)
    			break;
    	}
    	if(t==3)
    		return nums[i];
    	else {
    		return nums[nums.length-1];
    	}
    }

}
